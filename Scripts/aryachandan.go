// This is a simple "Hello, World!" program in Go.
// The main package is required for Go executable programs.
package main

// Importing the 'fmt' package to use the Println function for printing output.
import "fmt"

// The main function is the entry point of the executable program.
func main() {
	// The Println function from the 'fmt' package prints the string "Hello, World!" to the console.
	fmt.Println("Hello, World!")
}
